def numeros_al_final_recursivo(lista: List[Union[float, str]]) -> List[Union[float, str]]:
    """CHALLENGE OPCIONAL - Re-escribir de forma recursiva."""
    def _agrupar(lista, strings, numeros):
        if not lista:
            return strings + numeros
        if isinstance(lista[0], (float, int)):
            numeros.append(lista[0])
        elif isinstance(lista[0], str):
            strings.append(lista[0])
        return _agrupar(lista[1:], strings, numeros)
    return _agrupar(lista, [], [])


